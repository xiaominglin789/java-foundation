public class Char001 {
    public static void main(String[] args) {
        char c1 = 'A';
        char c2 = '\t';
        char c3 = '中';
        char c4 = 97; // 直接转成字符'a'
        System.out.println(c1);
        System.out.println(c2);
        System.out.println(c3);
        System.out.println(c4);
    }
}